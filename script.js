let currentCharacter;
let currentQuoteIndex;
let attempts;
let usedQuoteIndexes = [];
let timeElapsed = 0;
let timerInterval;
let roundActive = true;

function pickRandomCharacter() {
    const index = Math.floor(Math.random() * quoteBank.length);
    return quoteBank[index];
}

function startRound() {
    clearInterval(timerInterval);
    timeElapsed = 0;
    document.getElementById("timer").textContent = "Time: " + timeElapsed;

    timerInterval = setInterval(() => {
        timeElapsed++;
        document.getElementById("timer").textContent = "Time: " + timeElapsed;
    }, 1000);

    currentCharacter = pickRandomCharacter();
    currentQuoteIndex = Math.floor(Math.random() * currentCharacter.lines.length);
    usedQuoteIndexes = [currentQuoteIndex];
    attempts = 0;
    const cards = document.querySelectorAll(".card");
    cards.forEach(card => {
        card.classList.remove("disabled")
    });
    document.getElementById("quote1").textContent = "(Quote hidden)";
    document.getElementById("quote2").textContent = "(Quote hidden)";
    document.getElementById("quote3").textContent = "(Quote hidden)";
    document.getElementById("quote1").className = "quote hidden-text";
    document.getElementById("quote2").className = "quote hidden-text";
    document.getElementById("quote3").className = "quote hidden-text";

    revealQuote(document.getElementById("quote1"), currentCharacter.lines[currentQuoteIndex])
}

function handleGuess(card) {
    const guess = card.dataset.name;
    if (guess === currentCharacter.speaker) {
        clearInterval(timerInterval);
        showResult("Correct! It was " + currentCharacter.speaker);
    } else {
        card.classList.add("disabled");
        attempts++;

        if (attempts === 1) {
            const newIndex = getNewQuoteIndex();
            const q2 = document.getElementById("quote2");
            revealQuote(q2, currentCharacter.lines[newIndex]);
        }
        else if (attempts === 2) {
            const newIndex = getNewQuoteIndex();
            const q3 = document.getElementById("quote3");
            revealQuote(q3, currentCharacter.lines[newIndex]);
        }
        else {
            clearInterval(timerInterval);
            showResult("Wrong! It was " + currentCharacter.speaker);        
        }
    }
}

function getNewQuoteIndex() {
    let newIndex = Math.floor(Math.random()*currentCharacter.lines.length);

    while (usedQuoteIndexes.includes(newIndex)) {
        newIndex = Math.floor(Math.random() * currentCharacter.lines.length);
    }

    usedQuoteIndexes.push(newIndex);
    return newIndex;
}

function revealQuote(element, text){
    element.style.opacity = 0;
    setTimeout(() => {
        element.textContent = text;
        element.className = "quote revealed";
        element.style.opacity = 1;

    }, 400);
}

startRound();

const cards = document.querySelectorAll(".card");
cards.forEach(card => {
    card.addEventListener("click", () => {
        if (!roundActive) return;
        handleGuess(card);
    });
});

function showResult(message) {
    roundActive = false;
    document.getElementById("quote1").textContent = "";
    document.getElementById("quote2").textContent = "";
    document.getElementById("quote3").textContent = "";

    document.getElementById("quote1").textContent = message;
    document.getElementById("quote1").className = "quote revealed";
    document.getElementById("next-round-btn").classList.remove("hidden");
}

document.getElementById("next-round-btn").addEventListener("click", () => {
    document.getElementById("next-round-btn").classList.add("hidden");
    roundActive = true;
    startRound();
});

